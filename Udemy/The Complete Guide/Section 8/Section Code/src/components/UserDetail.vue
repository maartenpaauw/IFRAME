<template>
    <div class="component">
        <h3>You may view the User Details here</h3>
        <p>Many Details</p>
        <p>User name: {{ switchName() }}</p>
        <button @click="resetName">Reset name</button>
    </div>
</template>

<script>
    export default {
        props: {
            name: {
                type: String,
                required: true,
                default: 'Maarten'
            }
        },
        methods: {
            switchName () {
                return this.name.split("").reverse().join("");
            },
            resetName () {
                this.$emit('nameWasReset', 'Maarten');
            }
        }
    }
</script>

<style scoped>
    div {
        background-color: lightcoral;
    }
</style>
